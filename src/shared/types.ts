/**
 * Record
 * This utility type is used to replace the original 'object' type which can become difficult to
 * deal with.
 */
type IRecord<T> = {
  [key: string]: T;
};





/* ************************************************************************************************
 *                                         MODULE EXPORTS                                         *
 ************************************************************************************************ */
export type {
  IRecord,
};
